<?php

namespace App\Utils;

use InvalidArgumentException;
class StringHelper
{
    private const unwanted_array_accent = ['Š' => 'S', 'š' => 's', 'Ž' => 'Z', 'ž' => 'z', 'À' => 'A', 'Á' => 'A', 'Â' => 'A', 'Ã' => 'A', 'Ä' => 'A', 'Å' => 'A', 'Æ' => 'A', 'Ç' => 'C', 'È' => 'E', 'É' => 'E',
        'Ê' => 'E', 'Ë' => 'E', 'Ì' => 'I', 'Í' => 'I', 'Î' => 'I', 'Ï' => 'I', 'Ñ' => 'N', 'Ò' => 'O', 'Ó' => 'O', 'Ô' => 'O', 'Õ' => 'O', 'Ö' => 'O', 'Ø' => 'O', 'Ù' => 'U',
        'Ú' => 'U', 'Û' => 'U', 'Ü' => 'U', 'Ý' => 'Y', 'Þ' => 'B', 'ß' => 'Ss', 'à' => 'a', 'á' => 'a', 'â' => 'a', 'ã' => 'a', 'ä' => 'a', 'å' => 'a', 'æ' => 'a', 'ç' => 'c',
        'è' => 'e', 'é' => 'e', 'ê' => 'e', 'ë' => 'e', 'ì' => 'i', 'í' => 'i', 'î' => 'i', 'ï' => 'i', 'ð' => 'o', 'ñ' => 'n', 'ò' => 'o', 'ó' => 'o', 'ô' => 'o', 'õ' => 'o',
        'ö' => 'o', 'ø' => 'o', 'ù' => 'u', 'ú' => 'u', 'û' => 'u', 'ý' => 'y', 'þ' => 'b', 'ÿ' => 'y', ];

    private const unwanted_array_cachekey = [
        '{' => '', '}' => '', '(' => '', ')' => '', '/' => '_', '\\' => '_', '@' => '', ':' => '', ' ' => '',
    ];

    public static function strRemoveAccent(string $str): string
    {
        return strtr($str, self::unwanted_array_accent);
    }

    public static function strRemoveCacheKey(string $str): string
    {
        $t = array_merge(self::unwanted_array_accent, self::unwanted_array_cachekey);

        return strtolower(strtr($str, $t));
    }

    public static function GenreateRandomString(int $length): string
    {
        if ($length < 0) {
            throw new InvalidArgumentException('$length can\'t be negative.');
        }
        if (0 == $length) {
            return '';
        }

        return substr(str_shuffle(str_repeat($x = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ', (int) ceil($length / \strlen($x)))), 1, $length);
    }
}
